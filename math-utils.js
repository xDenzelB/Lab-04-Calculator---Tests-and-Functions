export function add(someNumber, someOtherNumber) {
    return someNumber + someOtherNumber;
}
export function subtract(someNumber, someOtherNumber) {
    return someNumber - someOtherNumber;
}

export function multiply(someNumber, someOtherNumber) {
    return someNumber * someOtherNumber;
}

export function divide(someNumber, someOtherNumber) {
    return someNumber / someOtherNumber;
}
